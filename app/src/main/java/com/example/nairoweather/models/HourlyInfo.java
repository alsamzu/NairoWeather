
package com.example.nairoweather.models;

import java.io.Serializable;
import java.util.List;
import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class HourlyInfo implements Serializable
{

    @SerializedName("summary")
    @Expose
    private String summary;
    @SerializedName("icon")
    @Expose
    private String icon;
    @SerializedName("data")
    @Expose
    private List<HourlyData> data = null;
    private final static long serialVersionUID = -5847603251542909984L;

    /**
     * No args constructor for use in serialization
     *
     */
    public HourlyInfo() {
    }

    /**
     *
     * @param summary
     * @param data
     * @param icon
     */
    public HourlyInfo(String summary, String icon, List<HourlyData> data) {
        super();
        this.summary = summary;
        this.icon = icon;
        this.data = data;
    }

    public String getSummary() {
        return summary;
    }

    public void setSummary(String summary) {
        this.summary = summary;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public List<HourlyData> getData() {
        return data;
    }

    public void setData(List<HourlyData> data) {
        this.data = data;
    }

}
